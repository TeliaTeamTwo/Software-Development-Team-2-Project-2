import axios from 'axios';
import { setAlert } from './alert';

import { GET_EMPLOYEE_PROFILE, PROFILE_ERROR, UPDATE_EMPLOYEE_PROFILE } from './types';

// Get current employee profile
export const getCurrentEmployeeProfile = () => async (dispatch) => {
  try {
    const res = await axios.get('api/employeeprofile/me');

    dispatch({
      type: GET_EMPLOYEE_PROFILE,
      payload: res.data,
    });
  } catch (err) {
    dispatch({
      type: PROFILE_ERROR,
      payload: { msg: err.response.statusText, status: err.response.status },
    });
  }
};

// Create or update profile
export const createEmployeeProfile = (
  formData,
  history,
  edit = false
) => async dispatch => {
  try {
    const config = {
      headers: {
        'Content-Type': 'application/json'
      }
    };

    const res = await axios.post('api/employeeprofile', formData, config);

    dispatch({
      type: GET_EMPLOYEE_PROFILE,
      payload: res.data,
    });

    dispatch(setAlert(edit ? 'Profile Updated' : 'Profile Created', 'success'));

    if (!edit) {
      history.push('/dashboard');
    }
  } catch (err) {
    const errors = err.response.data.errors;

    if (errors) {
      errors.forEach(error => dispatch(setAlert(error.msg, 'danger')));
    }

    dispatch({
      type: PROFILE_ERROR,
      payload: { msg: err.response.statusText, status: err.response.status }
    });
  }
};

